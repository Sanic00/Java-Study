package ch06;

public class Time {
	private int hour;
	private int minute;
	private float second;

	public int getHour() {
		return hour;
	}

	public int getMinute() {
		return minute;
	}

	public float getSecond() {
		return second;
	}

	public void setHour(int hour) {
		this.hour = hour;
	}

	public void setMinute(int minute) {
		this.minute = minute;
	}

	public void setSecond(float second) {
		this.second = second;
	}

}
